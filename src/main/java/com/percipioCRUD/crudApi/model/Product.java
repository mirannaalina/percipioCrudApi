package com.percipioCRUD.crudApi.model;

public class Product {

    private String name;
    private String sku;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSku() {
        return sku;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }
}
